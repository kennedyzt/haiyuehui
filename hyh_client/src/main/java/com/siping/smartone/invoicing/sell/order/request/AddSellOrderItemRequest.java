package com.siping.smartone.invoicing.sell.order.request;

import java.io.Serializable;

public class AddSellOrderItemRequest implements Serializable {
    private static final long serialVersionUID = -4597059595006967837L;
    private String materialId; // 物料
    private String productNo; // 商品货号
    private String batchNumber; // 批次号
    private String productionDate; // 生产日期
    private Double counts; // 数量
    private Double purchasePrice; // 采购价格
    private Double sellPrice; // 销售单价
    private Double discount; // 折扣
    private Double afterDiscount; // 折扣
    private Double taxRate; // 税率
    private Double tax; // 税额
    private Double total; // 总计
    private Boolean isGift; // 是否赠品
    private Double shipmentsAmount; // 发货数量
    private String remark; // 备注
    private String shipmentsDate; // 发货时间

    public String getMaterialId() {
        return materialId;
    }

    public void setMaterialId(String materialId) {
        this.materialId = materialId;
    }

    public String getBatchNumber() {
        return batchNumber;
    }

    public void setBatchNumber(String batchNumber) {
        this.batchNumber = batchNumber;
    }

    public String getProductionDate() {
        return productionDate;
    }

    public void setProductionDate(String productionDate) {
        this.productionDate = productionDate;
    }

    public Double getCounts() {
        return counts;
    }

    public void setCounts(Double counts) {
        this.counts = counts;
    }

    public Double getPurchasePrice() {
        return purchasePrice;
    }

    public void setPurchasePrice(Double purchasePrice) {
        this.purchasePrice = purchasePrice;
    }

    public Double getSellPrice() {
        return sellPrice;
    }

    public void setSellPrice(Double sellPrice) {
        this.sellPrice = sellPrice;
    }

    public Double getDiscount() {
        return discount;
    }

    public void setDiscount(Double discount) {
        this.discount = discount;
    }

    public Double getTaxRate() {
        return taxRate;
    }

    public void setTaxRate(Double taxRate) {
        this.taxRate = taxRate;
    }

    public Double getTax() {
        return tax;
    }

    public void setTax(Double tax) {
        this.tax = tax;
    }

    public Double getTotal() {
        return total;
    }

    public void setTotal(Double total) {
        this.total = total;
    }

    public Boolean getIsGift() {
        return isGift;
    }

    public void setIsGift(Boolean isGift) {
        this.isGift = isGift;
    }

    public Double getShipmentsAmount() {
        return shipmentsAmount;
    }

    public void setShipmentsAmount(Double shipmentsAmount) {
        this.shipmentsAmount = shipmentsAmount;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getShipmentsDate() {
        return shipmentsDate;
    }

    public void setShipmentsDate(String shipmentsDate) {
        this.shipmentsDate = shipmentsDate;
    }

    public Double getAfterDiscount() {
        return afterDiscount;
    }

    public void setAfterDiscount(Double afterDiscount) {
        this.afterDiscount = afterDiscount;
    }

    public String getProductNo() {
        return productNo;
    }

    public void setProductNo(String productNo) {
        this.productNo = productNo;
    }
}
