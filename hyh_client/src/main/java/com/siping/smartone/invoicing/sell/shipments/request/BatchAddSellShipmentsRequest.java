package com.siping.smartone.invoicing.sell.shipments.request;

import java.io.Serializable;
import java.util.List;

public class BatchAddSellShipmentsRequest implements Serializable {
    private static final long serialVersionUID = 1L;
    private List<String> orderNumbers;
    private Integer loginId;
    private String ip; // 跟业务无关，用于记录日志
    private String mac; // 跟业务无关，用于记录日志

    public List<String> getOrderNumbers() {
        return orderNumbers;
    }

    public void setOrderNumbers(List<String> orderNumbers) {
        this.orderNumbers = orderNumbers;
    }

    public Integer getLoginId() {
        return loginId;
    }

    public void setLoginId(Integer loginId) {
        this.loginId = loginId;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getMac() {
        return mac;
    }

    public void setMac(String mac) {
        this.mac = mac;
    }
}
