package com.siping.smartone.invoicing.inventory.inventory.response;

import java.io.Serializable;

public class GetInventoryListResponse implements Serializable {
    private static final long serialVersionUID = -1977232815053669853L;
    private String orderNumber;
    private String sellFlowId;
    private Boolean isDraft;
    private String billsDate; // 单据日期
    private String partnerName;
    private String summary;
    private Integer counts;
    private Double totalPrice;
    private String storageName;
    private String nickname;

    public String getOrderNumber() {
        return orderNumber;
    }

    public void setOrderNumber(String orderNumber) {
        this.orderNumber = orderNumber;
    }

    public String getSellFlowId() {
        return sellFlowId;
    }

    public void setSellFlowId(String sellFlowId) {
        this.sellFlowId = sellFlowId;
    }

    public Boolean getIsDraft() {
        return isDraft;
    }

    public void setIsDraft(Boolean isDraft) {
        this.isDraft = isDraft;
    }

    public String getBillsDate() {
        return billsDate;
    }

    public void setBillsDate(String billsDate) {
        this.billsDate = billsDate;
    }

    public String getPartnerName() {
        return partnerName;
    }

    public void setPartnerName(String partnerName) {
        this.partnerName = partnerName;
    }

    public String getSummary() {
        return summary;
    }

    public void setSummary(String summary) {
        this.summary = summary;
    }

    public Integer getCounts() {
        return counts;
    }

    public void setCounts(Integer counts) {
        this.counts = counts;
    }

    public Double getTotalPrice() {
        return totalPrice;
    }

    public void setTotalPrice(Double totalPrice) {
        this.totalPrice = totalPrice;
    }

    public String getStorageName() {
        return storageName;
    }

    public void setStorageName(String storageName) {
        this.storageName = storageName;
    }

    public String getNickname() {
        return nickname;
    }

    public void setNickname(String nickname) {
        this.nickname = nickname;
    }
}
