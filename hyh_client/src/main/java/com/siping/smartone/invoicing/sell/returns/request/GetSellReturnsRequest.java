package com.siping.smartone.invoicing.sell.returns.request;

import java.io.Serializable;

public class GetSellReturnsRequest implements Serializable {
    private static final long serialVersionUID = -9160736006395318565L;
    private String returnsNumber;
    private String sellFlowId;

    public GetSellReturnsRequest() {
    }

    public GetSellReturnsRequest(String returnsNumber, String sellFlowId) {
        this.returnsNumber = returnsNumber;
        this.sellFlowId = sellFlowId;
    }

    public String getReturnsNumber() {
        return returnsNumber;
    }

    public void setReturnsNumber(String returnsNumber) {
        this.returnsNumber = returnsNumber;
    }

    public String getSellFlowId() {
        return sellFlowId;
    }

    public void setSellFlowId(String sellFlowId) {
        this.sellFlowId = sellFlowId;
    }

}
