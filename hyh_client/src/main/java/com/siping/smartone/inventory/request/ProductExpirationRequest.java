package com.siping.smartone.inventory.request;

import java.io.Serializable;

public class ProductExpirationRequest implements Serializable {

    private static final long serialVersionUID = 5465042013518598699L;
    private Integer supplierId;
    private String remainDays; // 剩余过期天数
    private Integer pageNo;
    private Integer pageSize;
    private Boolean isPaging = true;

    public Integer getSupplierId() {
        return supplierId;
    }

    public void setSupplierId(Integer supplierId) {
        this.supplierId = supplierId;
    }

    public Integer getPageNo() {
        return pageNo;
    }

    public void setPageNo(Integer pageNo) {
        this.pageNo = pageNo;
    }

    public Integer getPageSize() {
        return pageSize;
    }

    public void setPageSize(Integer pageSize) {
        this.pageSize = pageSize;
    }

    public String getRemainDays() {
        return remainDays;
    }

    public void setRemainDays(String remainDays) {
        this.remainDays = remainDays;
    }

    public Boolean getIsPaging() {
        return isPaging;
    }

    public void setIsPaging(Boolean isPaging) {
        this.isPaging = isPaging;
    }

}
