package com.siping.smartone.inventory.request;

import java.io.Serializable;

public class GetInventoryStatusRequest implements Serializable {
    private static final long serialVersionUID = 6831321850919214510L;
    private String storageId;
    private String materialName;
    private Integer materialTypeId;
    private Boolean isMaxInventory;
    private Boolean isMinInventory;
    private Integer pageNo;
    private Integer pageSize;
    private String materialNo;

    public String getStorageId() {
        return storageId;
    }

    public void setStorageId(String storageId) {
        this.storageId = storageId;
    }

    public String getMaterialName() {
        return materialName;
    }

    public void setMaterialName(String materialName) {
        this.materialName = materialName;
    }

    public Integer getMaterialTypeId() {
        return materialTypeId;
    }

    public void setMaterialTypeId(Integer materialTypeId) {
        this.materialTypeId = materialTypeId;
    }

    public Boolean getIsMaxInventory() {
        return isMaxInventory;
    }

    public void setIsMaxInventory(Boolean isMaxInventory) {
        this.isMaxInventory = isMaxInventory;
    }

    public Boolean getIsMinInventory() {
        return isMinInventory;
    }

    public void setIsMinInventory(Boolean isMinInventory) {
        this.isMinInventory = isMinInventory;
    }

    public Integer getPageNo() {
        return pageNo;
    }

    public void setPageNo(Integer pageNo) {
        this.pageNo = pageNo;
    }

    public Integer getPageSize() {
        return pageSize;
    }

    public void setPageSize(Integer pageSize) {
        this.pageSize = pageSize;
    }

    public String getMaterialNo() {
        return materialNo;
    }

    public void setMaterialNo(String materialNo) {
        this.materialNo = materialNo;
    }
}
