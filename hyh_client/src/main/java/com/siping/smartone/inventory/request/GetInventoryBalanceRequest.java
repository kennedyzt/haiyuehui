package com.siping.smartone.inventory.request;

import java.io.Serializable;

public class GetInventoryBalanceRequest implements Serializable {
    private static final long serialVersionUID = -5167501523260240949L;
    private String billsDate;
    private String material; // 商品货号
    private String materialType;
    private String stroage; // 仓库编码
    private Integer pageNo;
    private Integer pageSize;
    private String materialName;
    private String materialTypeName;
    private String stroageName; // 仓库编码
    private Boolean isPaging = true;

    public String getBillsDate() {
        return billsDate;
    }

    public void setBillsDate(String billsDate) {
        this.billsDate = billsDate;
    }

    public String getMaterial() {
        return material;
    }

    public void setMaterial(String material) {
        this.material = material;
    }

    public Boolean getIsPaging() {
        return isPaging;
    }

    public void setIsPaging(Boolean isPaging) {
        this.isPaging = isPaging;
    }

    public String getMaterialType() {
        return materialType;
    }

    public void setMaterialType(String materialType) {
        this.materialType = materialType;
    }

    public String getStroage() {
        return stroage;
    }

    public void setStroage(String stroage) {
        this.stroage = stroage;
    }

    public Integer getPageNo() {
        return pageNo;
    }

    public void setPageNo(Integer pageNo) {
        this.pageNo = pageNo;
    }

    public Integer getPageSize() {
        return pageSize;
    }

    public void setPageSize(Integer pageSize) {
        this.pageSize = pageSize;
    }

    public String getMaterialName() {
        return materialName;
    }

    public void setMaterialName(String materialName) {
        this.materialName = materialName;
    }

    public String getMaterialTypeName() {
        return materialTypeName;
    }

    public void setMaterialTypeName(String materialTypeName) {
        this.materialTypeName = materialTypeName;
    }

    public String getStroageName() {
        return stroageName;
    }

    public void setStroageName(String stroageName) {
        this.stroageName = stroageName;
    }
}
