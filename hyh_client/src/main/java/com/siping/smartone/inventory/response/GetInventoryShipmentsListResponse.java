package com.siping.smartone.inventory.response;

import java.io.Serializable;

public class GetInventoryShipmentsListResponse implements Serializable {
    private static final long serialVersionUID = -1977232815053669853L;
    private String shipmentsNumber;
    private String sellFlowId;
    private Boolean isDraft;
    private String billsDate; // 单据日期
    private String summary;
    private Double counts;
    private Double totalPrice;
    private String storageName;
    private String nickname;

    public String getShipmentsNumber() {
        return shipmentsNumber;
    }

    public void setShipmentsNumber(String shipmentsNumber) {
        this.shipmentsNumber = shipmentsNumber;
    }

    public String getSellFlowId() {
        return sellFlowId;
    }

    public void setSellFlowId(String sellFlowId) {
        this.sellFlowId = sellFlowId;
    }

    public Boolean getIsDraft() {
        return isDraft;
    }

    public void setIsDraft(Boolean isDraft) {
        this.isDraft = isDraft;
    }

    public String getBillsDate() {
        return billsDate;
    }

    public void setBillsDate(String billsDate) {
        this.billsDate = billsDate;
    }

    public String getSummary() {
        return summary;
    }

    public void setSummary(String summary) {
        this.summary = summary;
    }

    public Double getCounts() {
        return counts;
    }

    public void setCounts(Double counts) {
        this.counts = counts;
    }

    public Double getTotalPrice() {
        return totalPrice;
    }

    public void setTotalPrice(Double totalPrice) {
        this.totalPrice = totalPrice;
    }

    public String getStorageName() {
        return storageName;
    }

    public void setStorageName(String storageName) {
        this.storageName = storageName;
    }

    public String getNickname() {
        return nickname;
    }

    public void setNickname(String nickname) {
        this.nickname = nickname;
    }
}
