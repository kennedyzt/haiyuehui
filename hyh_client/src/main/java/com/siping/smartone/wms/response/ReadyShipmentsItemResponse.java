package com.siping.smartone.wms.response;

import java.io.Serializable;

import com.siping.smartone.wms.ReadyShipmentsItemBase;

public class ReadyShipmentsItemResponse extends ReadyShipmentsItemBase implements Serializable {
    private static final long serialVersionUID = 1L;
    private String barcode;
    private String materialNo;
    private String materialName;
    private String unitName;
    private String confirmAmount;
    private Integer storageId;
    private String specificationsModel;
    private String locationName;
    private String expirationDate;
    private String trackingNo; // 物流运单号
    private String customsNumber; // 海关通关号
    private String ecOrderNumber; // 关联原始订单号

    public String getMaterialNo() {
        return materialNo;
    }

    public void setMaterialNo(String materialNo) {
        this.materialNo = materialNo;
    }

    public String getMaterialName() {
        return materialName;
    }

    public void setMaterialName(String materialName) {
        this.materialName = materialName;
    }

    public String getUnitName() {
        return unitName;
    }

    public void setUnitName(String unitName) {
        this.unitName = unitName;
    }

    public Integer getStorageId() {
        return storageId;
    }

    public void setStorageId(Integer storageId) {
        this.storageId = storageId;
    }

    public String getBarcode() {
        return barcode;
    }

    public void setBarcode(String barcode) {
        this.barcode = barcode;
    }

    public String getConfirmAmount() {
        return confirmAmount;
    }

    public void setConfirmAmount(String confirmAmount) {
        this.confirmAmount = confirmAmount;
    }

    public String getSpecificationsModel() {
        return specificationsModel;
    }

    public void setSpecificationsModel(String specificationsModel) {
        this.specificationsModel = specificationsModel;
    }

    public String getLocationName() {
        return locationName;
    }

    public void setLocationName(String locationName) {
        this.locationName = locationName;
    }

    public String getExpirationDate() {
        return expirationDate;
    }

    public void setExpirationDate(String expirationDate) {
        this.expirationDate = expirationDate;
    }

    public String getTrackingNo() {
        return trackingNo;
    }

    public void setTrackingNo(String trackingNo) {
        this.trackingNo = trackingNo;
    }

    public String getCustomsNumber() {
        return customsNumber;
    }

    public void setCustomsNumber(String customsNumber) {
        this.customsNumber = customsNumber;
    }

    public String getEcOrderNumber() {
        return ecOrderNumber;
    }

    public void setEcOrderNumber(String ecOrderNumber) {
        this.ecOrderNumber = ecOrderNumber;
    }
}
