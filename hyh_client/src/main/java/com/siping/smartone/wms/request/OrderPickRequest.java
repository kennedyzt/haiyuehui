package com.siping.smartone.wms.request;

import java.io.Serializable;
import java.util.List;

import com.siping.smartone.wms.response.ReadyShipmentsItemResponse;

public class OrderPickRequest implements Serializable {
    private static final long serialVersionUID = 1L;
    private Integer storageId;
    private List<ReadyShipmentsItemResponse> items;

    public Integer getStorageId() {
        return storageId;
    }

    public void setStorageId(Integer storageId) {
        this.storageId = storageId;
    }

    public List<ReadyShipmentsItemResponse> getItems() {
        return items;
    }

    public void setItems(List<ReadyShipmentsItemResponse> items) {
        this.items = items;
    }

}
