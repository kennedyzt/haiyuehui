package com.siping.smartone.permission.request;

import java.io.Serializable;
import java.util.List;

public class AddUserPermissionRequest implements Serializable {
    private static final long serialVersionUID = 5297538877500473810L;
    private Integer userId;
    private List<AddNodePermissionRequest> nodePermissionList;
    private Integer loginId;

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public Integer getLoginId() {
        return loginId;
    }

    public void setLoginId(Integer loginId) {
        this.loginId = loginId;
    }

    public List<AddNodePermissionRequest> getNodePermissionList() {
        return nodePermissionList;
    }

    public void setNodePermissionList(List<AddNodePermissionRequest> nodePermissionList) {
        this.nodePermissionList = nodePermissionList;
    }
}
