package com.siping.smartone.material.request;

import java.io.Serializable;

public class AddMaterialTypeRequest implements Serializable {
    private static final long serialVersionUID = 974397827584124159L;
    private Integer id; // 新增时，该字段为空
    private String typeName; // 物料类型名称
    private Integer parentId;
    private Integer level; // 节点的层次
    private Integer oldParentId; // 原来的父节点
    private Boolean isLeafNode; // 是否叶子结点
    private Boolean isDelete; // 是否删除
    private String description; // 描述
    private Integer createBy; // 新增时，该字段为空
    private Integer updateBy;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getTypeName() {
        return typeName;
    }

    public void setTypeName(String typeName) {
        this.typeName = typeName;
    }

    public Integer getParentId() {
        return parentId;
    }

    public void setParentId(Integer parentId) {
        this.parentId = parentId;
    }

    public Boolean getIsDelete() {
        return isDelete;
    }

    public void setIsDelete(Boolean isDelete) {
        this.isDelete = isDelete;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Integer getCreateBy() {
        return createBy;
    }

    public void setCreateBy(Integer createBy) {
        this.createBy = createBy;
    }

    public Integer getUpdateBy() {
        return updateBy;
    }

    public void setUpdateBy(Integer updateBy) {
        this.updateBy = updateBy;
    }

    public Boolean getIsLeafNode() {
        return isLeafNode;
    }

    public void setIsLeafNode(Boolean isLeafNode) {
        this.isLeafNode = isLeafNode;
    }

    public Integer getOldParentId() {
        return oldParentId;
    }

    public void setOldParentId(Integer oldParentId) {
        this.oldParentId = oldParentId;
    }

    public Integer getLevel() {
        return level;
    }

    public void setLevel(Integer level) {
        this.level = level;
    }

}
